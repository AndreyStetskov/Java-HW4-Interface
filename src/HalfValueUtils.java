public class HalfValueUtils {

    public static double halfValue(double value1, double value2) {
        double halfValue = (value1 + value2) / 2;
        return halfValue;
    }

    public static double halfValueOFThree(double value1, double value2, int value3) {
        double halfValue = (value1 + value2 + value3) / 3;
        return halfValue;
    }
}
